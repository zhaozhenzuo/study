    public List<List<Integer>> threeSum(int[] nums) {
        if (nums == null || nums.length < 3) {
            return new ArrayList<>();
        }

        //排序
        Arrays.sort(nums);

        List<List<Integer>> rList=new ArrayList<>();

        for(int pos=0;pos<=nums.length-3;pos++){
            if(pos>0 && nums[pos] == nums[pos-1]){
                //去重
                continue;
            }
            int left=pos+1;
            int right=nums.length-1;
            while(true){
                if(left>=right){
                    break;
                }
                if(left>pos+1 && nums[left] == nums[left-1]){
                    //left前一个元素与left当前元素相等则跳过
                    left++;
                    continue;
                }
                int sum=nums[pos]+nums[left]+nums[right];
                if(sum==0){
                    List<Integer> tList=new ArrayList<>();
                    tList.add(nums[pos]);
                    tList.add(nums[left]);
                    tList.add(nums[right]);
                    rList.add(tList);
                    left++;
                }else if(sum>0){
                    //加起来值大于0，说明需要的是更小的元素，右指针减少
                    right--;
                }else{
                    //加起来值小于0，说明需要更大的值，左指针增加
                    left++;
                }
            }
        }
        return rList;
    }

