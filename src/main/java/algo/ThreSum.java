	public List<List<Integer>> threeSum(int[] nums) {

		if (nums == null || nums.length < 3) {
			return new ArrayList<List<Integer>>();
		}

		// 正数map
		Map<Integer/* 数值 */, Integer/* 对应出现次数 */> posiveMap = new HashMap<Integer, Integer>();

		// 负数map
		Map<Integer, Integer> negativeMap = new HashMap<Integer, Integer>(nums.length);

		List<Integer> positiveList = new ArrayList<Integer>(nums.length);

		List<Integer> negtiveList = new ArrayList<Integer>(nums.length);

		for (int i = 0; i < nums.length; i++) {
			int value = nums[i];
			if (value >= 0) {
				Integer res = posiveMap.get(value);
				if (res == null) {
					res = 0;
					positiveList.add(value);

				}
				res = res + 1;
				posiveMap.put(value, res);

			} else {
				Integer res = negativeMap.get(value);
				if (res == null) {
					res = 0;
					negtiveList.add(value);
				}
				res = res + 1;
				negativeMap.put(value, res);
			}

		}

		List<List<Integer>> resList = new ArrayList<List<Integer>>(128);

		// 处理0的情况
		Set<String> resSet = new HashSet<String>();
		Integer zero = posiveMap.get(0);
		if (zero != null && zero >= 3) {
			resList.add(Arrays.asList(0, 0, 0));
			resSet.add("0|0|0");
		}
		
		if(negtiveList.size()<=0 || positiveList.size()<=0){
			return resList;
		}

		// 循环第一个列表
		for (int i = 0; i < positiveList.size(); i++) {

			for (int j = 0; j < negtiveList.size(); j++) {

				int positive = positiveList.get(i);
				int negtive = negtiveList.get(j);

				int add = positive + negtive;
				if (add > 0) {
					// 说明需要找一个相反的负数
					int negtiveAdd = -1 * add;

					int numShould = 1;
					if (negtiveAdd == negtive) {
						numShould = numShould + 1;
					}

					Integer num = negativeMap.get(negtiveAdd);
					if (num != null && num >= numShould) {

						List<Integer> tempList = Arrays.asList(positive, negtive, negtiveAdd);
						Collections.sort(tempList);

						String key = tempList.get(0) + "|" + tempList.get(1) + "|" + tempList.get(2);

						if (resSet.contains(key)) {
							continue;
						}

						resList.add(tempList);
						resSet.add(key);
					}
				} else if (add < 0) {
					// 需要找一个正数
					int positiveAdd = -1 * add;

					int numShould = 1;
					if (positiveAdd == positive) {
						numShould = numShould + 1;
					}

					Integer num = posiveMap.get(positiveAdd);
					if (num != null && num >= numShould) {

						List<Integer> tempList = Arrays.asList(positive, negtive, positiveAdd);
						Collections.sort(tempList);

						String key = tempList.get(0) + "|" + tempList.get(1) + "|" + tempList.get(2);

						if (resSet.contains(key)) {
							continue;
						}

						resList.add(tempList);
						resSet.add(key);

					}
				} else {
					// add==0时,只有可能正数与负数相加，这时，需要从正数列表再找出一个0
					Integer temp = posiveMap.get(0);
					if (temp != null && temp > 0) {

						List<Integer> tempList = Arrays.asList(positive, negtive, 0);
						Collections.sort(tempList);
						String key = tempList.get(0) + "|" + tempList.get(1) + "|" + tempList.get(2);
						if (resSet.contains(key)) {
							continue;
						}

						resList.add(tempList);
						resSet.add(key);

					}

				}
			}
		}

		return resList;

	}

